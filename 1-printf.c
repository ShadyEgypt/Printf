/**
 * get_integer_length - count num of digits
 * @num: the number we need to find its len
 *
 * Return: len of int
 */
int get_integer_length(int num)
{
	int length = 0;

	if (num == 0)
	{
		return (1);
	}
	while (num != 0)
	{
		length++;
		num /= 10;
	}
	return (length);
}
